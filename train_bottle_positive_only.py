def ensure_real_checkpoint(path: Path):
    prefix = path.read_bytes()[:128]
    if prefix.startswith(b"version https://git-lfs.github.com/spec/v1"):
        raise RuntimeError(
            "Checkpoint is still a Git LFS pointer. Please download the real checkpoint before finetuning."
        )


def torch_load_compat(path, map_location="cpu"):
    signature = inspect.signature(torch.load)
    if "weights_only" in signature.parameters:
        return torch.load(path, map_location=map_location, weights_only=False)
    return torch.load(path, map_location=map_location)


def clip_mean_fill_rgb():
    return tuple(int(round(channel * 255)) for channel in OPENAI_DATASET_MEAN)


def pad_short_side_to_square(image: Image.Image, fill_rgb=None):
    if fill_rgb is None:
        fill_rgb = clip_mean_fill_rgb()

    width, height = image.size
    side = max(width, height)
    pad_left = (side - width) // 2
    pad_top = (side - height) // 2
    pad_right = side - width - pad_left
    pad_bottom = side - height - pad_top

    padded = ImageOps.expand(
        image,
        border=(pad_left, pad_top, pad_right, pad_bottom),
        fill=fill_rgb,
    )
    return padded


def get_cache_dtype(name: str):
    return torch.float16 if name == "float16" else torch.float32


def preprocess_image_for_visualad(image_path: Path, image_size: int) -> torch.Tensor:
    image_pil = Image.open(image_path).convert("RGB")
    padded_pil = pad_short_side_to_square(image_pil, fill_rgb=clip_mean_fill_rgb())
    resized_pil = TF.resize(padded_pil, [image_size, image_size], interpolation=InterpolationMode.BICUBIC)

    tensor = ToTensor()(resized_pil)
    tensor = Normalize(OPENAI_DATASET_MEAN, OPENAI_DATASET_STD)(tensor)
    return tensor


class BottleBinaryDataset(Dataset):
    def __init__(
        self,
        entries: List[Dict],
        image_size: int,
        cache_images: bool = True,
        cache_dtype: str = "float16",
        desc: str = "dataset",
    ):
        self.entries = entries
        self.image_size = image_size
        self.cache_images = cache_images
        self.cache_dtype = get_cache_dtype(cache_dtype)
        self.cached_items = None

        if self.cache_images:
            self.cached_items = []
            progress = tqdm(self.entries, desc=f"Caching {desc}", leave=False, mininterval=5.0)
            for item in progress:
                image_path = Path(item["path"])
                image_tensor = preprocess_image_for_visualad(image_path, self.image_size)
                self.cached_items.append(
                    {
                        "image": image_tensor.to(dtype=self.cache_dtype).contiguous(),
                        "label": float(item["label"]),
                        "path": str(image_path),
                        "source_name": item.get("source_name", "unknown"),
                        "defect_type": item.get("defect_type", "unknown"),
                    }
                )

    def __len__(self):
        return len(self.entries)

    def __getitem__(self, index):
        if self.cached_items is not None:
            cached = self.cached_items[index]
            image_tensor = cached["image"].float()
            label = torch.tensor(cached["label"], dtype=torch.float32)
            return {
                "image": image_tensor,
                "label": label,
                "path": cached["path"],
                "source_name": cached["source_name"],
                "defect_type": cached["defect_type"],
            }

        item = self.entries[index]
        image_path = Path(item["path"])
        image_tensor = preprocess_image_for_visualad(image_path, self.image_size)
        label = torch.tensor(float(item["label"]), dtype=torch.float32)
        return {
            "image": image_tensor,
            "label": label,
            "path": str(image_path),
            "source_name": item.get("source_name", "unknown"),
            "defect_type": item.get("defect_type", "unknown"),
        }


